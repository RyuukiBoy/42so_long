#include "ft_printf.h"

void	ft_putstr(char *s, int *len)
{
	int	i;

	i = 0;
	if (!s)
		ft_putstr("(null)", len);
	while (s && s[i])
	{
		ft_putchar(s[i], len);
		i++;
	}
}
